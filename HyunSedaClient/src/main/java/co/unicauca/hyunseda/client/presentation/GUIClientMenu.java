    private void initializeTable() {
        tblProducts.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Name", "Description", "Price", "ID"
                }
        ));
        tblProducts.getColumnModel().getColumn(3).setMinWidth(0);
        tblProducts.getColumnModel().getColumn(3).setMaxWidth(0);
        tblProducts.getColumnModel().getColumn(3).setWidth(0);
    }
    
    private void fillTable(List<Product> listProducts) {
        initializeTable();
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();

        Object rowData[] = new Object[4];//No columnas
        for (int i = 0; i < listProducts.size(); i++) {
            rowData[0] = listProducts.get(i).getName();
            rowData[1] = listProducts.get(i).getDescription();
            rowData[2] = String.valueOf(listProducts.get(i).getPrice());
            rowData[3] = listProducts.get(i).getProductId();
            
            model.addRow(rowData);
        }
    }
    
    private void fillTable(Product product) {
        initializeTable();
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();

        Object rowData[] = new Object[4];//No columnas
        rowData[0] = product.getName();
        rowData[1] = product.getDescription();
        rowData[2] = String.valueOf(product.getPrice());
        rowData[3] = product.getProductId();

        model.addRow(rowData);
    }
    
