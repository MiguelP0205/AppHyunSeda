    private void btnCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCartActionPerformed
            
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow != -1) { // Verificar si se ha seleccionado una fila
            long id = Long.parseLong(String.valueOf(tblProducts.getValueAt(selectedRow, 3)));
            
            GUICartConf.setProductId(ProductService.getProductService().getProductById(id).getProductId());
            GUICartConf.setProductName(ProductService.getProductService().getProductById(id).getName());
            GUICartConf.setSize(300, 200);
            GUICartConf.setVisible(true);
        }
    }//GEN-LAST:event_btnCartActionPerformed

